    private void chkBrowniesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkBrowniesActionPerformed
        // TODO add your handling code here:
        if (chkBrownies.isSelected()) {
            int qty = (Integer) spnBrownies.getValue();
            if (qty <= 0) {
                JOptionPane.showMessageDialog(this, "Please set quantity greater than 0!");
                chkBrownies.setSelected(false);
                return;
            }
            
            SizeOption selected = getSelectedSizeOption(cmbSizeBrownies, "Brownies");
            if (selected == null) {
                JOptionPane.showMessageDialog(this, "Please select a valid size!");
                chkBrownies.setSelected(false);
                return;
            }
            
            
            if (qty > selected.stock) {
                JOptionPane.showMessageDialog(this, 
                    "Only " + selected.stock + " items available!", 
                    "Insufficient Stock", 
                    JOptionPane.WARNING_MESSAGE);
                chkBrownies.setSelected(false);
                return;
            }
            
            if (parentMenu != null) {
                parentMenu.addToCart(
                    selected.variantId,
                    selected.productId, 
                    getProductIdByName("Brownies"),
                    "Brownies",
                    selected.sizeName,
                    selected.price,
                    qty
                );
            }
            
            JOptionPane.showMessageDialog(this, "Added: Brownies (" + selected.sizeName + ") x" + qty);
            chkBrownies.setSelected(false);
        }
    }//GEN-LAST:event_chkBrowniesActionPerformed

