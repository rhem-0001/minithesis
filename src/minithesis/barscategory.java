    class SizeOption {
        int variantId;
        String productId;
        String sizeName;
        double price;
        int stock;
        
        public SizeOption(int variantId, String productId, String sizeName, double price, int stock) {
        this.variantId = variantId;
        this.productId = productId;
        // If database size is null, default to "Standard" so it doesn't show "null"
        this.sizeName = (sizeName == null || sizeName.isEmpty()) ? "Standard" : sizeName;
        this.price = price;
        this.stock = stock;
    }

        @Override
    public String toString() {
        // This returns ONLY the size name to the Combo Box
        return sizeName; 
    }
    }
    
