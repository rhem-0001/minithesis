    private void resetDailySales() {
        Connection con = null;
        PreparedStatement archivePst = null;
        PreparedStatement checkPst = null;
        ResultSet rs = null;

        try {
            con = sqlconnector.getConnection();
            if (con == null) {
                JOptionPane.showMessageDialog(this, "Database connection failed!");
                return;
            }

            // Get yesterday's date
            Calendar calendar = Calendar.getInstance();
            calendar.add(Calendar.DAY_OF_YEAR, -1);
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String yesterdayStr = dateFormat.format(calendar.getTime());

            // First, check if data already exists for yesterday
            String checkQuery = "SELECT COUNT(*) as count FROM daily_sales WHERE sales_date = ?";
            checkPst = con.prepareStatement(checkQuery);
            checkPst.setString(1, yesterdayStr);
            rs = checkPst.executeQuery();

            boolean exists = false;
            if (rs.next()) {
                exists = rs.getInt("count") > 0;
            }
            rs.close();
            checkPst.close();

            if (exists) {
                // Update existing record
                String updateQuery = "UPDATE daily_sales SET total_orders = (SELECT COUNT(*) FROM orders WHERE DATE(order_date) = ?), " +
                                   "total_sales_amount = (SELECT COALESCE(SUM(total_amount), 0) FROM orders WHERE DATE(order_date) = ?) " +
                                   "WHERE sales_date = ?";
                archivePst = con.prepareStatement(updateQuery);
                archivePst.setString(1, yesterdayStr);
                archivePst.setString(2, yesterdayStr);
                archivePst.setString(3, yesterdayStr);
                archivePst.executeUpdate();
            } else {
                // Insert new record
                String archiveQuery = "INSERT INTO daily_sales (sales_date, total_orders, total_sales_amount) " +
                                    "SELECT ?, COUNT(*), COALESCE(SUM(total_amount), 0) FROM orders WHERE DATE(order_date) = ?";
                archivePst = con.prepareStatement(archiveQuery);
                archivePst.setString(1, yesterdayStr);
                archivePst.setString(2, yesterdayStr);
                archivePst.executeUpdate();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error archiving daily sales: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (archivePst != null) archivePst.close();
                if (checkPst != null) checkPst.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    
