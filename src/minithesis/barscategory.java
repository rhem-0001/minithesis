    private void loadProductSizes(String productName, javax.swing.JComboBox sizeCombo, javax.swing.JLabel priceLabel) {
        try {
        Connection con = sqlconnector.getConnection();
        String query = "SELECT pv.variant_id, p.product_id, s.size_name, pv.price, pv.stock_quantity " +
                      "FROM product_variant pv " +
                      "JOIN product p ON pv.product_id = p.product_id " +
                      "JOIN size s ON pv.size_id = s.size_id " +
                      "WHERE p.product_name = ? AND pv.stock_quantity > 0";
        
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, productName);
        ResultSet rs = pst.executeQuery();
        
        sizeCombo.removeAllItems();
        
        while(rs.next()) {
            SizeOption option = new SizeOption(
                rs.getInt("variant_id"),
                rs.getString("product_id"),
                rs.getString("size_name"),
                rs.getDouble("price"),
                rs.getInt("stock_quantity")
            );
            
            // Add ONLY SizeOption objects
            sizeCombo.addItem(option); 
        }
        
        if (sizeCombo.getItemCount() > 0) {
            sizeCombo.setSelectedIndex(0);
            SizeOption firstItem = (SizeOption) sizeCombo.getSelectedItem();
            priceLabel.setText(String.valueOf(firstItem.price));
            sizeCombo.setEnabled(true);
        } else {
            // DO NOT ADD A STRING HERE!
            // Just disable the combo box and set price to 0
            priceLabel.setText("0");
            sizeCombo.setEnabled(false);
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }
    
    private void addSizeChangeListener(javax.swing.JComboBox sizeCombo, javax.swing.JLabel priceLabel, String productName) {
        sizeCombo.addItemListener(new java.awt.event.ItemListener() {
        @Override
        public void itemStateChanged(java.awt.event.ItemEvent evt) {
            if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
                Object selectedObj = sizeCombo.getSelectedItem();
                
                if (selectedObj instanceof SizeOption) {
                    SizeOption selected = (SizeOption) selectedObj;
                    // Update the price label with the selected size's price
                    priceLabel.setText(String.format("%.2f", selected.price));
                }
            }
        }
    });
    }
    
    private void updatePriceLabel(javax.swing.JComboBox<String> combo, javax.swing.JLabel priceLabel) {
        if (combo.getSelectedItem() == null || priceLabel == null) return;
        
        String selectedText = combo.getSelectedItem().toString();
        if (selectedText.contains("Out of Stock")) {
            priceLabel.setText("₱0.00");
            return;
        }
        
        // Extract size name from display text (before " - ₱")
        String sizeName = selectedText.split(" - ")[0];
        
        try {
            Connection con = sqlconnector.getConnection();
            String query = "SELECT pv.price " +
                          "FROM product_variant pv " +
                          "JOIN product p ON pv.product_id = p.product_id " +
                          "JOIN size s ON pv.size_id = s.size_id " +
                          "WHERE p.product_name = ? AND s.size_name = ?";
            
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, getProductNameFromComponent(combo));
            pst.setString(2, sizeName);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                priceLabel.setText("₱" + rs.getDouble("price"));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private String getProductNameFromComponent(javax.swing.JComboBox<String> combo) {
        String name = combo.getName();
        if (name == null) return "";
        
        // Map component names to product names
        if (name.contains("Brownies") && !name.contains("Dark")) return "Brownies";
        if (name.contains("Butterscotch")) return "Butterscotch";
        if (name.contains("Cassava")) return "Cassava Bars";
        if (name.contains("Cheese")) return "Cheese Bars";
        if (name.contains("Revel")) return "Revel Bars";
        if (name.contains("Mango")) return "Mango Cake Bars";
        if (name.contains("Classic")) return "Classic Assorted Bars";
        if (name.contains("Premium")) return "Premium Assorted Bars";
        if (name.contains("Dark")) return "Dark Fudge Brownies";
        if (name.contains("Gods")) return "Food For The Gods";
        return "";
    }
    
